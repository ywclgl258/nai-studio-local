while ($r = $sth->fetch()) { $catCache[$r['slug']] = $r['id']; }

$inserted = 0;
$skipped = 0;
foreach ($TAGS as $entry) {
    [$name, $cat, $cn, $count] = $entry;
    if (!isset($catCache[$cat])) { $skipped++; continue; }
    $cid = $catCache[$cat];
    try {
        Db::pdo()->prepare(
            "INSERT INTO tags (name, category_id, cn_name, post_count) VALUES (?, ?, ?, ?)
             ON DUPLICATE KEY UPDATE cn_name = COALESCE(VALUES(cn_name), cn_name),
                                     category_id = VALUES(category_id),
                                     post_count = GREATEST(post_count, VALUES(post_count))"
        )->execute([$name, $cid, $cn, $count]);
        $inserted++;
    } catch (Exception $e) {
        $skipped++;
    }
}

